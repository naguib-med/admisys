    <section class="container mx-auto px-4">
        <h2 class="text-2xl font-bold text-gray-800 mb-4">Formation ADMISYS</h2>
        <p class="text-gray-600">La Licence ADMISYS (Administration et Sécurité des Systèmes et des Réseaux) est une formation axée sur l'administration des systèmes d'exploitation Linux et Windows, le déploiement et l'administration des applications en réseau, la gestion et l'organisation des accès réseau, la gestion du parc matériel et des sauvegardes, la gestion des utilisateurs, les techniques de virtualisation ainsi que la mise en place des politiques de sécurité.</p>

        <p class="text-gray-600">D'une durée de deux semestres, la formation combine des enseignements théoriques et pratiques, des projets tutorés et un stage en entreprise de 3 à 4 mois. Elle est conçue pour fournir un socle solide de connaissances fondamentales, complété par des compétences professionnelles et transversales.</p>

        <h3 class="text-xl font-bold text-gray-800 mt-8 mb-4">Objectifs de la formation</h3>
        <ul class="list-disc list-inside text-gray-600">
            <li>Maîtriser les environnements UNIX-Like (GNU/Linux, BSD*, ...) ainsi que les technologies Microsoft</li>
            <li>Savoir définir les besoins en réseaux d'une entreprise et mettre en place et configurer un réseau informatique</li>
            <li>Maitriser le déploiement d'un système d'information et en maintenir sa disponibilité</li>
            <li>Avoir une connaissance des principaux systèmes de gestion de bases de données, des langages de requête et des outils de programmation nécessaires</li>
            <li>Assurer la sécurité et la sûreté des données informatiques et de l'infrastructure qui héberge le système d'information</li>
            <li>Savoir manager un projet informatique et en assurer sa communication</li>
        </ul>

        <h3 class="text-xl font-bold text-gray-800 mt-8 mb-4">Conditions et modalités d'admission</h3>
        <ul class="list-disc list-inside text-gray-600">
            <li>Être titulaire d'un diplôme Bac + 2 (DEUG, DUT, BTS, DEUST) ou tout diplôme jugé équivalent par la commission pédagogique</li>
            <li>Candidatures via l'application eCandidat : <a href="https://candidatures.univ-perp.fr">https://candidatures.univ-perp.fr</a></li>
            <li>Dispositif Étude en France et Erasmus+ pour les étudiants étrangers</li>
            <li>Procédure de Validation des Acquis de l'Expérience (VAE)</li>
        </ul>

        <h3 class="text-xl font-bold text-gray-800 mt-8 mb-4">Programme pédagogique</h3>
        <ul class="list-disc list-inside text-gray-600">
            <li>Volume horaire : 450 heures (60 ECTS)</li>
            <li>Stage professionnel de 12 à 16 semaines - 12 ECTS à partir du mois d'avril, pouvant se faire à l'étranger</li>
            <li>Langues enseignées : Français et anglais (15 heures d'enseignements en langue étrangère)</li>
            <li>Projets tutorés : 120 heures de travail en autonomie et accompagnement par des tuteurs pédagogiques</li>
        </ul>

        <h3 class="text-xl font-bold text-gray-800 mt-8 mb-4">Modalités de contrôle des connaissances</h3>
        <p class="text-gray-600">La charte des examens peut être consultée sur le site de l'UPVD. Les modalités de contrôle de connaissances (proportion du contrôle continu, coefficient, etc.) sont votées par les conseils centraux et sont affichées dans les centres d'examen.</p>
